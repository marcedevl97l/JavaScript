// if else + If else

let nota = 12;

if (nota === 5) {
  console.log("Has obtenido un puntaje sobresaliente")
} else if (nota === 4) {
  console.log("Buen trabajo pero puedes mejorar");
} else if (nota === 3) {
  console.log("Mejorar las notas");
} else if (nota === 2) {
  console.log("Tienes que mejorar");
} else if (nota === 1) {
  console.log("Estas reprobado");
} else if (nota === 0) {
  console.log("Desaprobado");
} else {
  console.log("Error introduce una nota entre en el 1 y el 5")
}





